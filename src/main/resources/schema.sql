CREATE TABLE applications
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    APP_NAME  VARCHAR(256) NOT NULL,
    API_KEY   VARCHAR(256) NOT NULL,
    MAX_TOKEN_AMOUNT  BIGINT NOT NULL,
    MIN_TOKEN_AMOUNT  BIGINT NOT NULL,
    REGENDAYS  INTEGER NOT NULL,
    REGENHOURS  INTEGER NOT NULL,
    REGENMINS INTEGER NOT NULL,
    CHECK (MIN_TOKEN_AMOUNT <= MAX_TOKEN_AMOUNT)
);