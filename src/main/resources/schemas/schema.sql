DROP TABLE IF EXISTS applications CASCADE;
DROP TABLE IF EXISTS tokens CASCADE;

CREATE TABLE applications
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    APP_NAME  VARCHAR(256) NOT NULL,
    API_KEY   VARCHAR(256) NOT NULL UNIQUE,
    MAX_TOKEN_AMOUNT  BIGINT NOT NULL,
    MIN_TOKEN_AMOUNT  BIGINT NOT NULL,
    REGENDAYS  INTEGER NOT NULL,
    REGENHOURS  INTEGER NOT NULL,
    REGENMINS INTEGER NOT NULL,
    CHECK (MIN_TOKEN_AMOUNT <= MAX_TOKEN_AMOUNT)
);

CREATE TABLE tokens
(
    USER_ID  VARCHAR(256) NOT NULL,
    TOKEN_AMOUNT  BIGINT NOT NULL,
    LINKED_APP  VARCHAR(256) NOT NULL,
    UNIQUE(USER_ID, LINKED_APP),
    PRIMARY KEY (USER_ID, LINKED_APP),
    CONSTRAINT fk_user_tokens_app
        FOREIGN KEY (LINKED_APP) 
        REFERENCES applications(API_KEY)
);

CREATE INDEX idx_tokens_linked_app ON tokens(LINKED_APP);